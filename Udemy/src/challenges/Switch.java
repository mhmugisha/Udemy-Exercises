package challenges;

public class Switch {

	public static void main(String[] args) {
		char switchChar = 'k';
		
		switch(switchChar) {
		case 'A':
			System.out.println("A found");
			break;
		case 'B':
			System.out.println("B found");
			break;
		case 'C':
			System.out.println("C found");
			break;
		case 'D':
			System.out.println("D found");
			break;
		case 'E':
			System.out.println("E found");
			break;
		default:
			System.out.println("none found");
		break;
		}

	}

}
